#include <iostream>
#include <math.h>
typedef long long ll;
int main(int argc, const char* argv[]) {
  ll n;
  std::cin >> n;
  std::string s;
  for (int i = 0; i < n; i++) {
    s += 'a';
  }

  std::cout << s << std::endl;

}